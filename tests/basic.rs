#[cfg(test)]
use bilzaa2d::Bilzaa2d;

#[test]
fn start(){
    let b2d = Bilzaa2d::new();    
assert_eq!(b2d.shapes.len(),0);
}
#[test]
fn two(){
    let mut b2d = Bilzaa2d::new();    
    b2d.add_shape("some name");
assert_eq!(b2d.shapes.len(),1);
}
#[test]
fn three(){
    let mut b2d = Bilzaa2d::new();    
    b2d.add_shape("some name");
    b2d.add_shape("bla bla");
assert_eq!(b2d.shapes.len(),2);
}
